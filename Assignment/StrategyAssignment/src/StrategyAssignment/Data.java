package StrategyAssignment;

public class Data
{
    private String name;
    private String roll;
    private String number;

    public Data(String name, String roll, String number) {
        this.name = name;
        this.roll = roll;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getRoll() {
        return roll;
    }

    public String getNumber() {
        return number;
    }
}
